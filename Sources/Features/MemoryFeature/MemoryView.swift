public struct MemoryClip: Identifiable, Equatable, Sendable {
    public let id: String
    public let title: String
    public let sourceURL: URL?
    public let note: String?
    public let bodyText: String?
    public let createdAt: Date
    public let updatedAt: Date
    public let state: ClipState
    public let failureReason: String?
    public let failureRetryable: Bool
    public let indexPreview: String?
    /// Persisted ContentBreakdown (Script) JSON; nil for text/url clips or when not yet indexed.
    public let scriptJSON: String?
    /// Authoritative evidence-grounded storyboard JSON. Legacy Script remains a projection.
    public let storyboardJSON: String?
    public let activeRunID: String?
    public let qualityStatusRaw: String?
    /// Coarse content type, used to route the clip into the 剪藏 vs 拆解 library.
    public let sourceKind: ClipSourceKind

    public init(
        id: String,
        title: String,
        sourceURL: URL?,
        note: String?,
        bodyText: String?,
        createdAt: Date,
        updatedAt: Date,
        state: ClipState,
        failureReason: String?,
        failureRetryable: Bool,
        indexPreview: String?,
        scriptJSON: String? = nil,
        storyboardJSON: String? = nil,
        activeRunID: String? = nil,
        qualityStatusRaw: String? = nil,
        sourceKind: ClipSourceKind = .text
    ) {
        self.id = id
        self.title = title
        self.sourceURL = sourceURL
        self.note = note
        self.bodyText = bodyText
        self.createdAt = createdAt
        self.updatedAt = updatedAt
        self.state = state
        self.failureReason = failureReason
        self.failureRetryable = failureRetryable
        self.indexPreview = indexPreview
        self.scriptJSON = scriptJSON
        self.storyboardJSON = storyboardJSON
        self.activeRunID = activeRunID
        self.qualityStatusRaw = qualityStatusRaw
        self.sourceKind = sourceKind
    }

    /// Whether this item belongs to the 拆解 (video) library rather than 剪藏 (text/url).
    public var isVideoBreakdown: Bool {
        sourceKind == .video
    }

    /// Decoded breakdown, if this clip carries a persisted script.
    public var breakdown: Script? {
        ScriptCoding.decode(json: scriptJSON)
    }

    public var storyboard: StoryboardDocumentV2? {
        guard let storyboardJSON else { return nil }
        return try? JSONDecoder().decode(StoryboardDocumentV2.self, from: Data(storyboardJSON.utf8))
    }

    /// Human-meaningful title for lists/navigation: imported videos carry a UUID temp filename as
    /// their stored title, which is noise — prefer the breakdown's AI title when the stored one is
    /// such an auto-generated name.
    public var displayTitle: String {
        if Self.isAutoGeneratedName(title), let scriptTitle = breakdown?.title,
           !scriptTitle.trimmingCharacters(in: .whitespacesAndNewlines).isEmpty {
            return scriptTitle
        }
        return title
    }

    /// True for machine-generated import names (PHPicker temp files are bare UUIDs).
    public static func isAutoGeneratedName(_ name: String) -> Bool {
        UUID(uuidString: name.trimmingCharacters(in: .whitespacesAndNewlines)) != nil
    }

    /// Copy/share text handed off to external generators (豆包 / 即梦). Prefers the
    /// structured breakdown rendering, falling back to the plain indexed body text.
    public var handoffText: String {
        if let breakdown {
            return ScriptRendering.indexableText(breakdown)
        }
        return bodyText ?? ""
    }
}

