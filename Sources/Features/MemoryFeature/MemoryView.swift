private struct ClipCaptureSheet: View {
    enum Mode: String, CaseIterable, Identifiable {
        case text = "文本"
        case url = "链接"
        var id: String { rawValue }
    }

    let submit: (MemoryCaptureInput) -> Void
    let cancel: () -> Void

    @State private var mode: Mode = .text
    @State private var text = ""
    @State private var urlString = ""

    var body: some View {
        NavigationStack {
            Form {
                Picker("类型", selection: $mode) {
                    ForEach(Mode.allCases) { Text($0.rawValue).tag($0) }
                }
                .pickerStyle(.segmented)

                switch mode {
                case .text:
                    Section("文本") {
                        TextField("粘贴要剪藏的文本", text: $text, axis: .vertical)
                            .lineLimit(4...12)
                    }
                case .url:
                    Section("链接") {
                        TextField("https://…", text: $urlString)
                            .autocorrectionDisabled()
                            .textContentType(.URL)
                            #if os(iOS)
                            .textInputAutocapitalization(.never)
                            #endif
                        Text("剪藏时会联网抓取一次正文,之后问答离线。")
                            .font(.caption)
                            .foregroundStyle(.secondary)
                    }
                }
            }
            .navigationTitle("新建剪藏")
            .toolbar {
                ToolbarItem(placement: .cancellationAction) {
                    Button("取消", action: cancel)
                }
                ToolbarItem(placement: .confirmationAction) {
                    Button("保存") {
                        if let input = makeInput() {
                            submit(input)
                        }
                    }
                    .disabled(makeInput() == nil)
                }
            }
        }
    }

    private func makeInput() -> MemoryCaptureInput? {
        switch mode {
        case .text:
            let trimmed = text.trimmingCharacters(in: .whitespacesAndNewlines)
            return trimmed.isEmpty ? nil : .text(trimmed)
        case .url:
            let trimmed = urlString.trimmingCharacters(in: .whitespaces)
            guard let url = URL(string: trimmed), let scheme = url.scheme?.lowercased(),
                  scheme == "http" || scheme == "https" else {
                return nil
            }
            return .url(url)
        }
    }
}

private struct MemoryRow: View {
    let item: MemoryClip

    var body: some View {
        HStack(alignment: .top, spacing: 10) {
            #if os(iOS)
            // 拆解 rows lead with a poster frame (first shot midpoint) — instantly recognizable,
            // unlike the UUID filenames these clips used to be listed by.
            if item.isVideoBreakdown, let url = item.sourceURL, url.isFileURL {
                ShotThumbnail(
                    videoURL: url,
                    seconds: posterSeconds,
                    startSeconds: posterShot?.startSeconds ?? 0,
                    endSeconds: posterShot?.endSeconds ?? max(0.1, posterSeconds),
                    shotIndex: 0
                )
            }
            #endif

            VStack(alignment: .leading, spacing: 4) {
                HStack(alignment: .firstTextBaseline) {
                    Text(item.displayTitle)
                        .font(.headline)
                        .lineLimit(2)
                    Spacer(minLength: 12)
                    StateBadge(state: item.state)
                }

                if let subtitle, !subtitle.isEmpty {
                    Text(subtitle)
                        .font(.subheadline)
                        .foregroundStyle(.secondary)
                        .lineLimit(2)
                }

                Text(metaLine)
                    .font(.caption)
                    .foregroundStyle(.tertiary)
                    .lineLimit(1)
            }
        }
        .padding(.vertical, 4)
    }

    /// 拆解: the breakdown summary; 剪藏: body preview. Raw file paths are never shown.
    private var subtitle: String? {
        if item.isVideoBreakdown {
            let summary = item.breakdown?.summary.trimmingCharacters(in: .whitespacesAndNewlines)
            if let summary, !summary.isEmpty { return summary }
            return nil
        }
        return item.bodyText?.trimmingCharacters(in: .whitespacesAndNewlines)
    }

    /// Compact facts: web host for 剪藏 links, 分镜 count for 拆解, plus the date.
    private var metaLine: String {
        var parts: [String] = []
        if item.isVideoBreakdown {
            if let count = item.breakdown?.shots.count, count > 0 {
                parts.append("分镜 \(count)")
            }
        } else if let host = item.sourceURL?.host(), !host.isEmpty {
            parts.append(host)
        }
        parts.append(item.createdAt.formatted(date: .abbreviated, time: .omitted))
        return parts.joined(separator: " · ")
    }

    private var posterSeconds: Double {
        guard let first = posterShot else { return 0.5 }
        return (first.startSeconds + max(first.startSeconds, first.endSeconds)) / 2
    }

    private var posterShot: StoryboardShot? {
        item.breakdown?.shots.min(by: { $0.index < $1.index })
    }
}

